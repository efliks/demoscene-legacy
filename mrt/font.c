void PutString(SCREEN *s, char *str, int x, int y, BYTE col, int chcol) {
	int		i, j, k = 0, index;
	char	c, ccol;
	BYTE	*ptr = s->Buffer + x + y * s->Width, *oldptr, *f, t;

	while ((c = str[k++]) != 0) {
		if (c > 'a' && c < 'z') c += ('A' - 'a');
		index = c - 32;
		if (index < 0 || index > 63) index = 0;
		f = FontData + (index << 3);

		oldptr = ptr;
		ccol = col;

		for (i = 0; i < 8; i++) {
			t = *f;

			for (j = 7; j >= 0; j--) {
				if ((t >> j) & 1) *ptr = ccol;

				ptr++;
			}

			f++;
			ptr  += (s->Width - 8);
			ccol += chcol;
		}

		ptr = oldptr + 8;
	}
}

